
import UIKit

extension UIColor {
    static var primaryColor: UIColor {
        return UIColor(named: "PrimaryColor")!
    }
    static var secondaryColor: UIColor {
        return UIColor(named: "SecondaryColor")!
    }
    static var detailsColor: UIColor {
        return UIColor(named: "DetailsColor")!
    }
    static var appWhite: UIColor {
        return UIColor(named: "AppWhiteColor")!
    }
}
